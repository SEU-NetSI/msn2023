   <h2>Terms and Policies</h2>

   <p><u><i>Registration Policies for Authors</i></u></p>
   <ul>
       <li>Each Author Registration is valid for ONE paper.</li>
       <li>To be published in the MSN 2023 Conference Proceedings and to be eligible for publication in IEEE Xplore, at least one author of an accepted paper is required to register for the conference at the Author Registration Rate even if the author is a student, and present the paper at the conference.</li>
       <li>The accepted paper must be presented by an author of that paper at the conference.</li>
       <li>Papers without registered author(s) by 25 October 2023 will not be included in the conference program and proceedings.</li>
   </ul>
   <p><u><i>Registration Policies for All Attendees</i></u></p>
